public class Ejercicio1 {
    public static void main(String[] args) throws Exception {
        int i = 1;
        int j = 1;
        while (i < 2) {
            System.out.println(i);
            i++;
        };
        do {
            System.out.println(j);
            j++;
        } while (j < 2);        
    }    
} 
